import Phaser from 'phaser';

/** Pixel color type: a hex color string or 0 for transparent */
export type PixelColor = string | 0;

/**
 * Renders a 2D pixel art array into a Phaser texture.
 * Each cell in `data` is either a CSS hex color string or 0 (transparent).
 * @param scene  The Phaser scene to create the texture in
 * @param key    Texture key to register
 * @param data   2D array of pixel colors (row-major, [y][x])
 * @param scale  Pixels per cell (default 1)
 */
export function renderPixelSprite(
  scene: Phaser.Scene,
  key: string,
  data: PixelColor[][],
  scale: number = 1,
): void {
  const rows = data.length;
  const cols = Math.max(...data.map(r => r.length));
  const w = cols * scale;
  const h = rows * scale;

  const g = scene.add.graphics();
  for (let y = 0; y < rows; y++) {
    const row = data[y];
    for (let x = 0; x < row.length; x++) {
      const c = row[x];
      if (c === 0) continue;
      const color = Phaser.Display.Color.HexStringToColor(c).color;
      g.fillStyle(color, 1);
      g.fillRect(x * scale, y * scale, scale, scale);
    }
  }
  g.generateTexture(key, w, h);
  g.destroy();
}

// ---------------------------------------------------------------------------
// Helper: resolve indexed color data ('c0','c1',...) into direct color arrays
// ---------------------------------------------------------------------------
type IndexedPixel = string | 0;

function resolveColors(
  colors: string[],
  data: IndexedPixel[][],
): PixelColor[][] {
  return data.map(row =>
    row.map(cell => {
      if (cell === 0) return 0;
      if (typeof cell === 'string' && cell.startsWith('c')) {
        const idx = parseInt(cell.substring(1), 10);
        return colors[idx];
      }
      // Already a direct color string (e.g. '#ffffff')
      return cell;
    }),
  );
}

